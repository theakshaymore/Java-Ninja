public class Literals {
    public static void main(String[] args) {
        // literals

        int num1 = 0b0101;
        System.out.println(num1);

        int num2 = 0x7E;
        System.out.println(num2);

        int num3 = 10_00_00_000;
        System.out.println(num3);

        char c = 'a';
        // c = c + 1;
        System.out.println(c + 1);
    }
}
