public class TypeConversion {
    public static void main(String[] args) {

        // type conversion and casting
        int a = 33;
        byte b = 44;

        b = (byte) a;

        float c = 4.5f;
        int d = (int) c;
        System.out.println(d);

        int e = 300;
        System.out.println(e);

        int f = 257;
        byte g = (byte) f;
        System.out.println(g);

    }
}
